def get_nationality(country_code):
    """Get nationality for a country code"""
    info = get_country_info(country_code)
    if "error" in info:
        return info["error"]
    return info["nationality"]

def search_country(search_term):
    """Search country by name, code, or nationality"""
    search_term = search_term.lower()
    results = []
    
    for code, data in COUNTRIES.items():
        if (search_term in data["name"].lower() or 
            search_term == code.lower() or 
            search_term == data["alpha2"].lower() or
            search_term in data["nationality"].lower()):
            results.append({
                "alpha2": data["alpha2"],
                "alpha3": code,
                "name": data["name"],
                "nationality": data["nationality"]
            })
    
    return results

